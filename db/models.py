# ─────────────────────────────────────────────────────────────
class PriceRule(Base):
    __tablename__ = "price_rules"
    id: Mapped[int] = mapped_column(Integer, primary_key=True)
    restaurant_id: Mapped[int] = mapped_column(Integer, ForeignKey("restaurants.id", ondelete="CASCADE"))
    name: Mapped[str] = mapped_column(String(200))
    label: Mapped[str] = mapped_column(Text)
    description: Mapped[Optional[str]] = mapped_column(Text)
    rule_type: Mapped[str] = mapped_column(String(50))  # 'percentage_off' | 'fixed_off' | 'fixed_price'
    value: Mapped[Decimal] = mapped_column(Numeric(10, 2))
    applies_to: Mapped[str] = mapped_column(String(50))  # 'category' | 'item' | 'order' | 'all'
    applies_to_ids: Mapped[list[int]] = mapped_column(ARRAY(Integer), server_default="{}")
    valid_days: Mapped[list[int]] = mapped_column(ARRAY(Integer), server_default="{0,1,2,3,4,5,6}")
    valid_from: Mapped[Optional[datetime]] = mapped_column(Time)
    valid_until: Mapped[Optional[datetime]] = mapped_column(Time)
    valid_date_from: Mapped[Optional[datetime]] = mapped_column(Date)
    valid_date_until: Mapped[Optional[datetime]] = mapped_column(Date)
    priority: Mapped[int] = mapped_column(Integer, default=0, server_default="0")
    is_active: Mapped[bool] = mapped_column(Boolean, default=True)


